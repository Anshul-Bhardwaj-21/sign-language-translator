"""
Property-based tests for Authentication Service

**Validates: Requirements 48.3**

This module tests that JWT tokens generated by the authentication system
can always be successfully verified, ensuring the token generation and
verification process is consistent and reliable.
"""

import pytest
from datetime import datetime, timedelta, timezone
from hypothesis import given, settings, strategies as st
from jose import jwt

from auth_service import (
    create_access_token,
    create_refresh_token,
    verify_token,
    SECRET_KEY,
    ALGORITHM,
)


# ============================================================================
# Hypothesis Strategies
# ============================================================================

@st.composite
def valid_user_data_strategy(draw):
    """
    Generate valid user data for token generation.
    
    Creates diverse user IDs, emails, and names to test token generation
    across a wide range of valid inputs.
    """
    # Generate user ID (UUID-like string)
    user_id = draw(st.uuids()).hex
    
    # Generate email
    username = draw(st.text(
        alphabet=st.characters(whitelist_categories=('L', 'N'), min_codepoint=97, max_codepoint=122),
        min_size=3,
        max_size=20
    ))
    domain = draw(st.sampled_from(['example.com', 'test.org', 'demo.net', 'user.io']))
    email = f"{username}@{domain}"
    
    # Generate name with various character sets
    name = draw(st.text(
        alphabet=st.characters(whitelist_categories=('L', 'Zs')),
        min_size=1,
        max_size=100
    ).filter(lambda x: x.strip() != ''))
    
    return {
        'user_id': user_id,
        'email': email,
        'name': name
    }


# ============================================================================
# Property-Based Tests
# ============================================================================

class TestAuthenticationTokenProperties:
    """
    Property-based tests for JWT token generation and verification.
    
    **Validates: Requirements 48.3**
    """
    
    @given(user_data=valid_user_data_strategy())
    @settings(max_examples=200, deadline=None)
    def test_property_access_token_verification_succeeds(self, user_data):
        """
        Property 3: Token verification succeeds for all valid generated tokens.
        
        **Validates: Requirements 48.3**
        
        For any valid user data (user_id, email, name):
        1. Generate an access token
        2. Verify the token
        3. Assert verification succeeds and returns correct user data
        
        This ensures that all tokens generated by the system can be verified,
        maintaining the integrity of the authentication system.
        """
        user_id = user_data['user_id']
        email = user_data['email']
        name = user_data['name']
        
        # Step 1: Generate access token
        token = create_access_token(user_id, email, name)
        
        # Step 2: Verify the token
        payload = verify_token(token, expected_type="access")
        
        # Step 3: Assert verification succeeds with correct data
        assert payload.sub == user_id, f"User ID mismatch: {payload.sub} != {user_id}"
        assert payload.email == email, f"Email mismatch: {payload.email} != {email}"
        assert payload.name == name, f"Name mismatch: {payload.name} != {name}"
        assert payload.type == "access", f"Token type mismatch: {payload.type} != access"
        
        # Verify token has expiration and issued-at timestamps
        assert payload.exp > 0, "Token expiration not set"
        assert payload.iat > 0, "Token issued-at not set"
        
        # Verify expiration is in the future
        now = datetime.now(timezone.utc).timestamp()
        assert payload.exp > now, "Token is already expired"
    
